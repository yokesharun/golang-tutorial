package main

import "fmt"

func variables() {

	// declaring a new variable - string
	var stringVariable = "A variable"

	// declaring a new variable - boolean
	var boolVariable = true

	// declaring a new variable - integer
	var integerVariable = 1

	// shortest way to create a new variable and assign a value
	thisIsNewVariable := "Yes I'm new variable"

	fmt.Println(stringVariable)
	fmt.Println(boolVariable)
	fmt.Println(integerVariable)
	fmt.Println(thisIsNewVariable)
}

// Note: the string values and import packages name must be with in " "
